// Day 7: JavaScript Variables

// Declare variables
let myName = "Michael";
let myAge = 27;
let isStudent = true;
let myCountry = "Nigeria";

// Copy values between variables
let first = "One";
let second = "Two";
second = first;

console.log("My name is:", myName);
console.log("I am", myAge, "years old");
console.log("Am I a student?", isStudent);
console.log("I live in", myCountry);
console.log("Second variable is now:", second);

// Basic math
let sum = 10 + 10;
let difference = 45 - 33;
let product = 8 * 10;
let quotient = 66 / 33;
let remainder = 10 % 3;

console.log("Sum:", sum);
console.log("Difference:", difference);
console.log("Product:", product);
console.log("Quotient:", quotient);
console.log("Remainder:", remainder);